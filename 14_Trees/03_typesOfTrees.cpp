/*There are different types of tress:
1) Full Binary Tree –  each node has either 0 or 2 children.

2)  Complete Binary Tree – all the levels of the tree are filled with nodes except the last level. The last level must have each node as left as possible.

3) Perfect Binary Tree – all the leaf nodes should be on the same level.

4)Balanced Binary Tree –  the height of the tree should be at max log(N), where N is the number of nodes. The tree should not be skewed.

5)Degenerate Tree –  a skewed tree that is similar to a linked list where each node is connected to the left of the previous node and the tree goes leftwards only.

*/