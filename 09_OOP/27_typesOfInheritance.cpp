/* Types of Inheritance:
1)single inheritance : one base-one derived class.
ex: Vehicle-->Car
2)multi-level inheritance: classes are inherited by their base class and the class itself is
a base class for any other class.
ex: Vehicle---> Car ---->HondaCity
here,Car class is derived from Vehicle(Car:derived, Vehicle:Base)
and Honda is derived from Car(Honda:derived, Car:Base)

3)hierarchical Inheritance: many classes are derived from one base class.
ex: Vehicla-----> Car,Bus,Truck all are derived from Vehicle.

4)multiple inheritance: one derived class is inherited from many base classes.

5)hybrid inheritance: mixing of above type inheritances.
*/